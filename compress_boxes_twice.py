# The Big O for this function
# O(a + b), note that you have to add input sizes for different loops or statements
# Assume the size of the first list is a
# Consequently, suppose that the size of the second list is b
def compress_boxes_twice(boxes: list, boxes2: list):
    # Runs linearly for each item in the list
    for item in boxes:
        print(item)

    for item in boxes2:
        print(item)

